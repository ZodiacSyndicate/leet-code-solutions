/**
 * Definition for singly-linked list.
 * function ListNode(val) {
 *     this.val = val;
 *     this.next = null;
 * }
 */
/**
 * @param {ListNode} head
 * @return {boolean}
 */
var isPalindrome = function(head) {
  let [s1, s2] = ['', '']
  while (head) {
    s1 = `${s1}${head.val}`
    s2 = `${head.val}${s2}`
    head = head.next
  }
  return s1 === s2
}
